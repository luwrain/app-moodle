package org.luwrain.io.moodle.model;

public class MoodleFunction
{
	private String name;
	private String version;

	/**
	 * function name
	 */
	public String getName() {
		return name;
	}

	/**
	 * The version number of the component to which the function belongs
	 */
	public String getVersion() {
		return version;
	}

}
